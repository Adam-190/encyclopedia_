# Цвета
BG_COLOR = "#DCD7D0"  # Основной фон
TEXT_COLOR = "#01060F"  # Цвет текста
SECONDARY_BG = "#A6B1D1"  # Вторичный фон
BUTTON_COLOR = "#C0C9E2"  # Цвет кнопок

# Загрузка данных из файла 
def load_data(letter):
    # Здесь можно загрузить данные из текстового файла или JSON
    # Для примера возвращаем статьи из словаря по первой букве
    return [article for article in articles_data.keys() if article.startswith(letter)]

# Функция для открытия окна статей
def open_articles(letter):
    articles = load_data(letter)
    if not articles:
        messagebox.showinfo("Информация", f"Нет статей на букву '{letter}'")
        return
    
    article_window = tk.Toplevel(root)
    article_window.title(f"Статьи на букву '{letter}'")
    article_window.configure(bg=BG_COLOR)
    
    # Создаем Canvas и Scrollbar для прокрутки
    canvas = tk.Canvas(article_window, bg=BG_COLOR)
    scrollbar = tk.Scrollbar(article_window, orient="vertical", command=canvas.yview)
    scrollable_frame = tk.Frame(canvas, bg=BG_COLOR)
    
    scrollable_frame.bind(
        "<Configure>",
        lambda e: canvas.configure(
            scrollregion=canvas.bbox("all")
        )
    )
    
    canvas.create_window((0, 0), window=scrollable_frame, anchor="nw")
    canvas.configure(yscrollcommand=scrollbar.set, bg=BG_COLOR)
    
    for article in articles:
        btn = tk.Button(scrollable_frame, text=article, command=lambda a=article: open_article(a), 
                       width=30, height=2, bg=BUTTON_COLOR, fg=TEXT_COLOR, font=('Arial', 10))
        btn.pack(pady=5)
    
    back_btn = tk.Button(scrollable_frame, text="Назад", command=article_window.destroy,
                        bg=BUTTON_COLOR, fg=TEXT_COLOR, font=('Arial', 10))
    back_btn.pack(pady=10)
    
    canvas.pack(side="left", fill="both", expand=True)
    scrollbar.pack(side="right", fill="y")

# Функция для открытия окна чтения статьи
def open_article(article):
    article_content_window = tk.Toplevel(root)
    article_content_window.title(article)
    article_content_window.configure(bg=BG_COLOR)
    
    # Загрузка содержания статьи из словаря
    content = articles_data.get(article, "Описание статьи отсутствует.")
    
    # Создаем Text виджет с прокруткой
    text_frame = tk.Frame(article_content_window, bg=BG_COLOR)
    text_scroll = tk.Scrollbar(text_frame)
    text = tk.Text(text_frame, wrap=tk.WORD, yscrollcommand=text_scroll.set, 
                   padx=10, pady=10, bg=SECONDARY_BG, fg=TEXT_COLOR, font=('Arial', 12))
    
    text_scroll.config(command=text.yview)
    text.insert(tk.END, content)
    text.config(state=tk.DISABLED)  # Делаем текст только для чтения
    
    text.pack(side=tk.LEFT, fill=tk.BOTH, expand=True)
    text_scroll.pack(side=tk.RIGHT, fill=tk.Y)
    text_frame.pack(fill=tk.BOTH, expand=True)
    
    back_btn = tk.Button(article_content_window, text="Назад", command=article_content_window.destroy,
                        bg=BUTTON_COLOR, fg=TEXT_COLOR, font=('Arial', 10))
    back_btn.pack(pady=10)

